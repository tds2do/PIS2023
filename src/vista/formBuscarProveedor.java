    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        ProveedorDAO prov = new ProveedorDAO();
        //Proveedor modelo = new Proveedor();
        List<Proveedor> proveedores = new ArrayList();
        proveedores = prov.Buscar(txtNombre.getText());
        
        //JOptionPane.showMessageDialog(null, proveedores.size());
        DefaultTableModel model = (DefaultTableModel) tabListaProveedores.getModel();
        tabListaProveedores.removeAll();
        model.setRowCount(0);
        
        for(Proveedor pro :proveedores){
            
            model.addRow(new String[]{String.valueOf(pro.getIdProveedor()), pro.getRuc(), pro.getRazonSocial()
            });
            
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnSeleccionar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionar1ActionPerformed
        
        int fila = tabListaProveedores.getSelectedRow();
        if(fila >= 0){
            String id = (String) tabListaProveedores.getValueAt(fila, 0);
            String razonSocial = (String) tabListaProveedores.getValueAt(fila, 2);
            formRegistroEntradaAdd.txtIdProveedor.setText(id);
            formRegistroEntradaAdd.txtRazonSocial.setText(razonSocial);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Por favor, debe seleccionar un registro.");
        }
        
        
    }//GEN-LAST:event_btnSeleccionar1ActionPerformed

